"""
    bark_ratio(sp, d) -> Float32

Inside-bark / outside-bark DBH ratio for species `sp` at DBH `d` (bratio.f).
"""
@inline function bark_ratio(sp::Integer, d::Real)::Float32
    df = Float32(d)
    ratio = df > 0f0 ? (SN_BARK_COEF[1, sp] + SN_BARK_COEF[2, sp] * df) / df : 0.99f0
    ratio > 0.99f0 && (ratio = 0.99f0)
    ratio < 0.80f0 && (ratio = 0.80f0)
    return ratio
end

"""
    dg_bound(sp, dbh, ddg, sizcap) -> Float32

Bound a predicted diameter increment `ddg` by the species large-tree taper
(dlo/dhi) and the size cap (`sizcap` = MAXSP×4 SIZCAP matrix). dgbnd.f.
"""
@inline function dg_bound(sp::Integer, dbh::Real, ddg_in::Real, sizcap::AbstractMatrix)::Float32
    dlo = SN_DG_DLODHI[2*sp - 1]; dhi = SN_DG_DLODHI[2*sp]
    ddg = Float32(ddg_in); d = Float32(dbh)
    if d <= dlo
        # no adjustment
    elseif d > dhi
        ddg = 0.048f0
    else
        ddg = ddg * (1f0 + 0.90f0 * ((d - dlo) / (dlo - dhi)))
        ddg < 0.048f0 && (ddg = 0.048f0)
    end
    if (d + ddg) > sizcap[sp, 1] && sizcap[sp, 3] < 1.5f0
        ddg = sizcap[sp, 1] - d
        ddg < 0.01f0 && (ddg = 0.01f0)
    end
    return ddg
end
