"""
    mortality!(state, ::Southern; fint=5f0)

Compute and apply periodic mortality, reducing `trees.tpa`. Currently the
background (Hamilton) component; the Pretzsch density component is added with
SDICAL. `fint` is the cycle length in years.
"""
function mortality!(s::StandState, ::Southern; fint::Float32 = 5f0)
    t = s.trees
    @inbounds for i in 1:t.n
        p = t.tpa[i]
        p <= 0f0 && continue
        sp = t.species[i]; d = t.dbh[i]
        ri = 1f0 / (1f0 + exp(SN_MORT_B0[sp] + SN_MORT_B1[sp] * d))   # background rate
        # TODO(C4 density): rip = (t ≤ PMSDIL·SDIMAX-density || rn≤0) ? ri : rn  (Pretzsch)
        rip = ri
        deaths = p * (1f0 - (1f0 - rip)^fint)
        deaths > p && (deaths = p)
        t.tpa[i] = p - deaths
    end
    return s
end
